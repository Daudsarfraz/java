public class switchcase {
    public static void main(String[] args) {
        System.out.println("Well Come to Switch Case....");
        
        String fruit = "Apple";
        switch(fruit){

            // if break is not presents it will check all casee
            // and execute all cases
            case "Apple":
            System.out.println("This is an Apple...");
            //    break;

            case "Mango":
            System.out.println("This is Mango...");
            //  break;

            case "Banana":
            System.out.println("This is Banana...");
            // break;
            default:
            System.out.println("Nothing Sorry....");

        }
    }
    
}
