public class whileloop {
    public static void main(String[] args) {
        System.out.println("Well come to while loop....");
        /*
         * Syntax of while loop
         * we use when we donot know how many time 
         * should be run like online shopping 
           
        while(condition){
            // body
            // increment / decrement
        }

    
         */
        int num = 0;
        while(num<100){
            System.out.println(num);
            num++;
        }
    }
    
}
